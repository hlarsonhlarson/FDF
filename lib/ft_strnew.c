#include <stdlib.h>
#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;

	str = (char *)malloc(sizeof(char) * (size + 1));
	if (str)
		ft_bzero(str, size + 1);
	else
		return (NULL);
	return (str);
}
